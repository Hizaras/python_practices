value = int(input("Please enter first number: "))

value2 = int(input("please enter second number: "))

if value == value2:
    print(value, "is equal", value2)

if value != value2:
    print(value, "they are not equal", value2)

if value < value2:
    print(value, "is greater than", value2)

if value > value2:
    print(value, "is less than", value2)

if value <= value2:
    print(value, "is less than equal to", value2)

if value >= value2:
    print(value, "is greater than equal to", value2)
